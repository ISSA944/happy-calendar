/**
 * Возвращает праздник или wellness-тему дня.
 * Никогда не возвращает null — карточка должна быть видна КАЖДЫЙ день.
 *
 * @param date формат "DD.MM"
 */
export function resolveHoliday(date: string): HolidayEntry {
  const real = HOLIDAYS_FULL[date];
  if (real) return real;

  const dayOfYear = computeDayOfYear(date);
  return WELLNESS_THEMES[dayOfYear % WELLNESS_THEMES.length];
}

function computeDayOfYear(date: string): number {
  const [dayStr, monthStr] = date.split('.');
  const day = Number(dayStr);
  const month = Number(monthStr);
  if (!Number.isFinite(day) || !Number.isFinite(month)) return 0;
  // Approx (без учёта високосного года — для ротации этого достаточно)
  const daysBeforeMonth = [0, 31, 59, 90, 120, 151, 181, 212, 243, 273, 304, 334];
  return (daysBeforeMonth[month - 1] ?? 0) + day;
}
