export function getFullDateStr(): string {
  const now = new Date()
  const months = ['января', 'февраля', 'марта', 'апреля', 'мая', 'июня', 'июля', 'августа', 'сентября', 'октября', 'ноября', 'декабря']
  return `${now.getDate()} ${months[now.getMonth()]} ${now.getFullYear()}`
}
