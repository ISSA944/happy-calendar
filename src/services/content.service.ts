// ══════════ HELPERS ══════════

export function getTodayDateStr(): string {
  const now = new Date()
  return `${String(now.getDate()).padStart(2, '0')}.${String(now.getMonth() + 1).padStart(2, '0')}`
}

export function getTodayFormatted(): string {
  const now = new Date()
  const months = ['января', 'февраля', 'марта', 'апреля', 'мая', 'июня', 'июля', 'августа', 'сентября', 'октября', 'ноября', 'декабря']
  return `${now.getDate()} ${months[now.getMonth()]}`
}

export function getGreeting(): string {
  const h = new Date().getHours()
  if (h < 6) return 'Доброй ночи'
  if (h < 12) return 'Доброе утро'
  if (h < 18) return 'Добрый день'
  return 'Добрый вечер'
}

export function getTodayHoliday(): Holiday | null {
  return HOLIDAYS[getTodayDateStr()] ?? null
}

export function getRandomQuote(mood: string, excludeIndex?: number): { text: string; index: number } {
  const pool = MOOD_QUOTES[mood] ?? MOOD_QUOTES['Нейтрально']
  let idx: number
  if (pool.length <= 1) {
    idx = 0
  } else {
    do {
      idx = Math.floor(Math.random() * pool.length)
    } while (idx === excludeIndex)
  }
  return { text: pool[idx], index: idx }
}

